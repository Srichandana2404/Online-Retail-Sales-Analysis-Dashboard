create database retail_project;

use retail_project;

DROP TABLE retail_sales;

CREATE TABLE retail_sales (
InvoiceNo VARCHAR(20),
StockCode VARCHAR(20),
Description TEXT,
Quantity INT,
InvoiceDate DATETIME,
UnitPrice FLOAT,
CustomerID FLOAT,
Country VARCHAR(50),
Revenue FLOAT
);

